    public void loadTablebHd(List<HoaDon> list) {
        dtm = (DefaultTableModel) tbHd.getModel();
        dtm.setRowCount(0);
        for (HoaDon hd : list) {
            dtm.addRow(new Object[]{
                dtm.getRowCount() + 1, hd.getMa(), hd.getNgayTao(), hd.getChuTro().getTen(), hd.htTrangThai()
            });
        }
    }

    public void loadTablebCthd(List<ChiTietHoaDon> list) {
        dtm = (DefaultTableModel) tbCthd.getModel();
        dtm.setRowCount(0);
        for (ChiTietHoaDon c : list) {
            dtm.addRow(new Object[]{
                dtm.getRowCount() + 1, c.getHoaDon().getMa(), c.getCtdv().getMa(),
                c.getNgayTao(), c.getNgaySua(), c.getTongTien(), c.htTrangThai()
            });
        }
    }

    public void loadTablebDv(List<ChiTietDichVu> list) {
        dtm = (DefaultTableModel) tbDv.getModel();
        dtm.setRowCount(0);
        for (ChiTietDichVu d : list) {
            dtm.addRow(new Object[]{
                dtm.getRowCount() + 1, d.getMa(), d.getPhong().getTen(),
                d.getPhong().getGia(), d.getDien().getThanhTien(),
                d.getNuoc().getThanhTien(), d.getDichVu().getThanhTien(), d.getNgayTao(), d.getNgaySua()
            });
        }
    }

