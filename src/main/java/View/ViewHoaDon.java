    private void btTaoHdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btTaoHdActionPerformed
        // TODO add your handling code here:
        HoaDon hd = new HoaDon();
        ChuTro ct = new ChuTro();
        String ma = "HD" + svH.genMaHD();
        Date ngayTao = java.util.Calendar.getInstance().getTime();
        UUID idCt = svH.findByIdCt("chien");
        ct.setId(idCt);
        int tinhTrang = 0;
        hd.setMa(ma);
        hd.setNgayTao(ngayTao);
        hd.setChuTro(ct);
        hd.setTinhTrang(tinhTrang);
        svH.addHd(hd);
        loadTablebHd(svH.listHoaDon());
    }//GEN-LAST:event_btTaoHdActionPerformed

    private void tbHdMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbHdMouseClicked
        // TODO add your handling code here:
        int index = tbHd.getSelectedRow();
        String ma = tbHd.getValueAt(index, 1).toString();
        txtHdMa.setText(ma);
        loadTablebCthd(svH.listCthd(ma));
    }//GEN-LAST:event_tbHdMouseClicked

    private void tbDvMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbDvMouseClicked
        // TODO add your handling code here:
        int index = tbDv.getSelectedRow();
        String ma = tbDv.getValueAt(index, 1).toString();
        txtCtdvMa.setText(ma);
        double tienPhong = (double) tbDv.getValueAt(index, 3);
        double tienDien = (double) tbDv.getValueAt(index, 4);
        double tienNuoc = (double) tbDv.getValueAt(index, 5);
        double tienDv = (double) tbDv.getValueAt(index, 6);
        double tongTien = tienPhong + tienDien + tienNuoc + tienDv;
        txtTongTien.setText(String.valueOf(tongTien));
    }//GEN-LAST:event_tbDvMouseClicked

    private void btThanhToanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btThanhToanActionPerformed
        // TODO add your handling code here:
        ChiTietHoaDon c = new ChiTietHoaDon();
        int index = tbHd.getSelectedRow();
        HoaDon hd = svH.listHoaDon().get(index);
        hd.setTinhTrang(1);
        svH.addHd(hd);
        loadTablebHd(svH.listHoaDon());
        ChiTietDichVu d = new ChiTietDichVu();
        String maHd = txtHdMa.getText();
        String maCtdv = txtCtdvMa.getText();
        UUID idHd = svH.findByIdHd(maHd);
        UUID idCtdv = svH.findByIdDv(maCtdv);
        double tongTien = Double.parseDouble(txtTongTien.getText());
        c.setIdHd(idHd);
        c.setIdCtdv(idCtdv);
        c.setNgayTao(java.util.Calendar.getInstance().getTime());
        c.setNgaySua(java.util.Calendar.getInstance().getTime());
        c.setTrangThai(1);
        c.setTongTien(tongTien);
        svH.addHdct(c);
        loadTablebCthd(svH.listCthd(maHd));
    }//GEN-LAST:event_btThanhToanActionPerformed

