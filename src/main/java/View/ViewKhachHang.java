    public void loadTable(List<KhachHang> list) {
        dtm = (DefaultTableModel) jTable2.getModel();
        dtm.setRowCount(0);
        for (KhachHang k : list) {
            dtm.addRow(new Object[]{
                k.getId(), k.getMa(), k.getTen(), k.getGioiTinh(),
                k.getSdt(), k.getDiaChi(), k.getEmail(), k.getNgayTao(), k.getNgaySua(),
                k.htTrangThai()
            });
        }

    }

