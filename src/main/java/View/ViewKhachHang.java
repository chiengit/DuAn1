    private void btThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btThemActionPerformed
        // TODO add your handling code here:
        if (txtMa.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Mã không được để trống");
        } else if (txtTen.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Tên không được để trống");
        } else if (!rdoNam.isSelected() && !rdoNu.isSelected()) {
            JOptionPane.showMessageDialog(this, "Giới tính không được để trống");
        } else if (txtSdt.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Sdt không được để trống");
        } else if (txtDiaChi.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Địa chỉ không được để trống");
        } else if (txtEmail.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "CCCD không được để trống");
        } else if (jdNgayTao.getDate() == null) {
            JOptionPane.showMessageDialog(this, "Ngày tạo không được để trống");
        } else if (jdNgaySua.getDate() == null) {
            JOptionPane.showMessageDialog(this, "Ngày sửa không được để trống");
        } else if (!rdoCo.isSelected() && !rdoKhong.isSelected()) {
            JOptionPane.showMessageDialog(this, "Trạng thái không được để trống");
        } else if (!rdoCo.isSelected() && !rdoKhong.isSelected()) {
            JOptionPane.showMessageDialog(this, "Trạng thái không được để trống");
        } else if (txtMa.getText().length() > 20) {
            JOptionPane.showMessageDialog(this, "Mã không được vượt quá 20 kí tự");
        } else if (txtTen.getText().length() > 20) {
            JOptionPane.showMessageDialog(this, "Tên không được vượt quá 20 kí tự");
        } else if (txtSdt.getText().length() > 20) {
            JOptionPane.showMessageDialog(this, "Sdt không được vượt quá 20 kí tự");
        } else if (txtDiaChi.getText().length() > 20) {
            JOptionPane.showMessageDialog(this, "Địa chỉ không được vượt quá 20 kí tự");
        } else if (txtTen.getText().matches("^[a-z A-Z]+") == false) {
            JOptionPane.showMessageDialog(this, "Tên phải là chữ");
        } else if (txtSdt.getText().matches("^(0?)(3[2-9]|5[6|8|9]|7[0|6-9]|8[0-6|8|9]|9[0-4|6-9])[0-9]{7}$") == false) {
            JOptionPane.showMessageDialog(this, "Số điện thoại không đúng định dạng");
        } else if (txtEmail.getText().matches("^[0-9]{12,12}+") == false) {
            JOptionPane.showMessageDialog(this, "CCCD phải là số và không quá 12 ký tự");
        } else if (svK.findByMa(txtMa.getText()).equalsIgnoreCase(txtMa.getText())) {
            JOptionPane.showMessageDialog(this, "Mã đã tồn tại");
        } else {
            KhachHang k = new KhachHang();
            k.setMa(txtMa.getText());
            k.setTen(txtTen.getText());
            k.setDiaChi(txtDiaChi.getText());
            k.setSdt(txtSdt.getText());
            k.setEmail(txtEmail.getText());
            if (rdoCo.isSelected()) {
                k.setTrangThai(1);
            } else {
                k.setTrangThai(0);
            }
            if (rdoNam.isSelected()) {
                k.setGioiTinh("Nam");
            } else {
                k.setGioiTinh("Nữ");
            }
            k.setNgayTao(jdNgayTao.getDate());
            k.setNgaySua(jdNgaySua.getDate());
            svK.save(k);
            loadTable(svK.getAllData());
        }
    }//GEN-LAST:event_btThemActionPerformed

    private void jTable2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable2MouseClicked
        // TODO add your handling code here:
        int index = jTable2.getSelectedRow();
        txtMa.setText(jTable2.getValueAt(index, 1).toString());
        txtTen.setText(jTable2.getValueAt(index, 2).toString());
        if (jTable2.getValueAt(index, 3).toString().equalsIgnoreCase("Nam")) {
            rdoNam.setSelected(true);
        } else {
            rdoNu.setSelected(true);
        }
        txtSdt.setText(jTable2.getValueAt(index, 4).toString());
        txtDiaChi.setText(jTable2.getValueAt(index, 5).toString());
        txtEmail.setText(jTable2.getValueAt(index, 6).toString());
        jdNgayTao.setDate((Date) jTable2.getValueAt(index, 7));
        jdNgaySua.setDate((Date) jTable2.getValueAt(index, 8));
        if (jTable2.getValueAt(index, 9).toString().equalsIgnoreCase("Có")) {
            rdoCo.setSelected(true);
        } else {
            rdoKhong.setSelected(true);
        }

    }//GEN-LAST:event_jTable2MouseClicked

